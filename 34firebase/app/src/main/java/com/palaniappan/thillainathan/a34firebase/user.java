package com.palaniappan.thillainathan.a34firebase;

public class user {
    public user() {
    }

    String name;
    String email;

    public user(String name, String email) {
        this.name = name;
        this.email = email;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }
}
